////////////////////////////////////////////////////////////////////////BUTTON 1
$(document).ready(function(){
$('#show-image1').on("click", function(){
  $('#button1').toggle('slow');
  });
});


$("#show-image1").click(function() {
  
   var lable = $("#show-image1").text().trim();

   if(lable == "Show") {
     $("#show-image1").text("Hide");
     $("#button1").hide();
   }
   else {
     $("#show-image1").text("Show");
     $("#button1").show();
   }
    
  });

